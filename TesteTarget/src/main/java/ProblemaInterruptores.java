public class ProblemaInterruptores {

    public static void resolverProblema() {
        System.out.println(
                "Solução do problema dos interruptores e lâmpadas:\n" +
                        "1. Ligue o primeiro interruptor e espere alguns minutos.\n" +
                        "2. Desligue o primeiro interruptor e ligue o segundo.\n" +
                        "3. Vá até a sala das lâmpadas.\n" +
                        "4. A lâmpada que estiver acesa é controlada pelo segundo interruptor.\n" +
                        "5. A lâmpada que estiver apagada, mas quente, é controlada pelo primeiro interruptor.\n" +
                        "6. A lâmpada que estiver apagada e fria é controlada pelo terceiro interruptor."
        );
    }
}
